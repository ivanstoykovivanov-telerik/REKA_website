for (let i = 0; i < projects.length; i++) {
    //get from local storage : 
    let page = localStorage.getItem("page"); 
    console.log(page);
    if(projects[i].id === page ){
        
        //CAPROUSSEL IMAGES 
        for (let j = 0; j < projects[i].caroussel_images.length; j++) {
            
            if(j === 0 ){
                $(".carousel-inner").append(`
                    <div class="carousel-item active">
                        <img class="d-block w-100" src="Image/${projects[i].caroussel_images[j]}" alt="Slide_${j}">
                    </div>
                `); 
            }else{
                $(".carousel-inner").append(`
                    <div class="carousel-item">
                        <img class="d-block w-100" src="Image/${projects[i].caroussel_images[j]}" alt="Slide_${j}">
                    </div>
                `);
            }
        }
        
        //SYNOPSIS
        let synopsisEl = document.querySelector("#synopsis");
        synopsisEl.innerHTML= projects[i].synopsis; 
        
        //CAST
        for (let j = 0; j < projects[i].cast.length; j++) {
            $("#cast_id").append(`
            <p>${projects[i].cast[j]}</p>
            `); 
        }

        //TEAM
        const entries = Object.entries(projects[i].team);
        for(const [role, person] of entries){
            const newRole = role.replace(/_/g, " "); 
            $("#team_id").append(`
                <p>${newRole} : ${person}</p>
            `);     
        }; 

        //STATS_1
        for (let j = 0; j < projects[i].stats_1.length; j++) {
            $("#stats_1_id").append(`
            <p>${projects[i].stats_1[j]}</p>
            `); 
        }

        //STATS_2
        const entries2 = Object.entries(projects[i].stats_2);
        if(entries2.length > 0){
            for(const [key, value] of entries2){
                // const newRole = role.replace(/_/g, " "); 
                $("#stats_2_id").append(`
                    <p>${key} : ${value}</p>
                `);
            };
        };

        //AWARDS 
        if(projects[i].awards.length > 0 ){
            for (let j = 0; j < projects[i].awards.length; j++) {
                $("#awards_id").append(`
                <p><img src="Image/video-camera.png">${projects[i].awards[j]}</p>   
                `); 
            }
        }

         //TITLE 
         $('#film_title').html(`${projects[i].title.toUpperCase()}`); 

        //POSTER 
        if(projects[i].poster !== ""){
            $("#poster_id").attr("src",`poster/${projects[i].poster}`);
        }

        //TRAILER
        $("#trailer_id").attr("src",`trailer/${projects[i].trailer}`);

    };  
}; 